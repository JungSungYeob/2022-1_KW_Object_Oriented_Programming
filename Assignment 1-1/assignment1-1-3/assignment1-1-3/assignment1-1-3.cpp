#include <iostream>

using namespace std;

int main() {
    int num1, num2;
    cout << "Enter Two Numbers :";
    cin >> num1 >> num2;
    cout << num1 << " + " <<num2<<": "<<num1+num2<<endl;
    cout << num1 << " - " <<num2<<": "<<num1-num2<<endl;
    cout << num1 << " * " <<num2<<": "<<num1*num2<<endl;
    cout << num1 << " / " <<num2<<": "<<(double)num1/(double)num2<<endl;
    cout << num1 << " % " <<num2<<": "<<num1%num2<<endl;

    return 0;
}
